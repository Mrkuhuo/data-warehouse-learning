CREATE  TABLE ods.ods_cart_info_full
(
    `id`           VARCHAR(255) COMMENT '编号',
    `k1`           DATE NOT NULL   COMMENT '分区字段',
    `user_id`      STRING COMMENT '用户id',
    `sku_id`       STRING COMMENT 'sku_id',
    `cart_price`   DECIMAL(16, 2) COMMENT '放入购物车时价格',
